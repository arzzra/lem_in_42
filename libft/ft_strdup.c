#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*str2;
	size_t	i;
	size_t	n;

	i = 0;
	n = ft_strlen(str);
	if (!(str2 = (char*)malloc((n + 1) * sizeof(char))))
		return (NULL);
	while (str[i] != '\0')
	{
		str2[i] = str[i];
		i++;
	}
	str2[i] = '\0';
	return (str2);
}
