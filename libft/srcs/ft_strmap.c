#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	unsigned int	i;
	char			*str;
	size_t			x;

	i = 0;
	if (s == NULL || !(f))
		return (NULL);
	x = ft_strlen(s);
	if ((str = (char*)malloc((x + 1) * sizeof(char))) == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
